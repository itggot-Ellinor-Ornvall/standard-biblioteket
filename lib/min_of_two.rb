# number x- det tal som ska jämföras med ett annat tal för att avgöra vilket av de två talen som är minst
# number y- det tal som ska jämföras med ett annat tal för att avgöra vilket av de två talen som är minst

def min_of_two(x, y)
    if x<y
        return x
    else
        return y
    end 
end
